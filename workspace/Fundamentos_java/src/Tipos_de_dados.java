
public class Tipos_de_dados {

	public static void main(String[] args) {
		int n1; //dados do tipo inteiro
		float n2; //dados do tipo real para dados curtos
		double n3; //dados do tipo real para dados longos
		char caracter; //dados do tipo string para mapear caractere
		String texto; //dados do tipo string para "mapear" textos
		
		n1 = 2;
		n2 = 10.5f;
		n3 = 11111.123;
		caracter = 'f'; //tipo char utilza aspas simples '';
		texto = "teste"; //tipo string utiliza aspas duplas "";
	}

}
